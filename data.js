function getDoneQuestions() {
  try { return JSON.parse(localStorage.getItem('cpa_done_questions') || '[]'); }
  catch(e) { return []; }
}
function markQuestionDone(questionId, chapterId) {
  const done = getDoneQuestions();
  if (!done.includes(questionId)) done.push(questionId);
  localStorage.setItem('cpa_done_questions', JSON.stringify(done));
  // 更新章节计数
  let chapterCounts;
  try { chapterCounts = JSON.parse(localStorage.getItem('cpa_chapter_counts') || '{}'); }
  catch(e) { chapterCounts = {}; }
  const key = String(chapterId);
  const chapterDoneIds = JSON.parse(localStorage.getItem('cpa_chapter_done_' + key) || '[]');
  if (!chapterDoneIds.includes(questionId)) {
    chapterDoneIds.push(questionId);
    localStorage.setItem('cpa_chapter_done_' + key, JSON.stringify(chapterDoneIds));
  }
  chapterCounts[key] = chapterDoneIds.length;
  localStorage.setItem('cpa_chapter_counts', JSON.stringify(chapterCounts));
}
function getChapterDoneCount(chapterId) {
  try {
    const counts = JSON.parse(localStorage.getItem('cpa_chapter_counts') || '{}');
    return parseInt(counts[String(chapterId)] || 0);
  } catch(e) { return 0; }
}

